package ejercicios_recuperacion;

public class Decimosexto {

	public static void main(String[] args) {
		for (int i=1; i<=51;i++)
		System.out.println("Recorrido del 1 al 51: "+i);
		int i=51;
		while(true){
			
			i++;
			System.out.println("Recorrido del 51 al 100: "+i);
			if(i==100){
				break;
			}
		}
		
	}

}
